    private void btnIngresarLlegadaVueloActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarLlegadaVueloActionPerformed
        if(lstFrecuencias.getItemCount() > 0) {
            int confirmarAccion = JOptionPane.showConfirmDialog(this, "Seguro deseas ingresar llegada de vuelo?");

            switch(confirmarAccion) {
                case 0: {
                    String frecuenciaSeleccionada = lstFrecuencias.getSelectedItem();
                    String[] f = frecuenciaSeleccionada.split(" - ");
                    controladorSistema.ingresarLlegadaVuelo(f, (Aeropuerto)this.usuarioLogeado.getTrabajo());
                    cargarFrecuenciasAprobadas();
                    this.vistaMonitoreo.cargarLlegadas();
                }
                default: {
                    JOptionPane.getRootFrame().dispose();
                }
            }
        }
    }//GEN-LAST:event_btnIngresarLlegadaVueloActionPerformed

