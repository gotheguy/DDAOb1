    public void cargarFrecuenciasAprobadas() {
        lstFrecuencias.removeAll();
        ArrayList<Frecuencia> frecuencias = controladorSistema.getListaFrecuenciasAprobadasDestino((Aeropuerto)this.usuarioLogeado.getTrabajo());
        
        for(Frecuencia f : frecuencias) {
            for (Vuelo v : f.getVuelos()) {
                if(v.getHoraLlegada() == null && v.getHoraPartida() != null) {
                    lstFrecuencias.add(v.getNumero() + " - " + f.getAeropuertoOrigen().getNombre() + " - " + v.getHoraPartida() + " - " + v.getEstado());   
                }
            }
        }
        lstFrecuencias.select(0);
    }
        
